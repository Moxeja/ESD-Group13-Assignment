public class EmployeeInfo {
    public final String eID;
    public final String eName;
    public final String euName;
    public final String role;
    
    public EmployeeInfo(String eID, String eName, String euName, String role){
    this.eID = eID;
    this.eName = eName;
    this.euName = euName;
    this.role = role;
            
    }
    
}
    
    
